    public void displayTodaysSalesIntoTable() {
        DefaultTableModel model = (DefaultTableModel) tblTodaySales.getModel();
        model.setRowCount(0);
        Object[] row = new Object[7];
        try {
            Date date = Calendar.getInstance().getTime();
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String date1 = dateFormat.format(date);
            Date today = new SimpleDateFormat("yyyy-MM-dd").parse(date1);
            List<Sales> list = ReportService.getSalesByToday(today);
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getProductName();
                row[1] = list.get(i).getProductCode();
                row[2] = list.get(i).getQty();
                row[3] = list.get(i).getUnitprice();
                row[4] = list.get(i).getTotalPrice();
                row[5] = list.get(i).getSalesdate();
                row[6] = list.get(i).getPurchase().getProductCategory().getName();

                model.addRow(row);
            }
        } catch (Exception e) {
        }

    }

    public void displaySalesByDateRangeIntoTable() {
        DefaultTableModel model = (DefaultTableModel) tblSalesByDateRange.getModel();
        model.setRowCount(0);
        Object[] row = new Object[7];
        try {
            Date sdate = new SimpleDateFormat("yyyy-MM-dd").parse(txtStartDate.getText().trim());
            Date edate = new SimpleDateFormat("yyyy-MM-dd").parse(txtEndDate.getText().trim());
            List<Sales> list = ReportService.getSalesByDateRange(sdate, edate);
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getProductName();
                row[1] = list.get(i).getProductCode();
                row[2] = list.get(i).getQty();
                row[3] = list.get(i).getUnitprice();
                row[4] = list.get(i).getTotalPrice();
                row[5] = list.get(i).getSalesdate();
                row[6] = list.get(i).getPurchase().getProductCategory().getName();

                model.addRow(row);
            }

        } catch (Exception e) {
        }

    }

