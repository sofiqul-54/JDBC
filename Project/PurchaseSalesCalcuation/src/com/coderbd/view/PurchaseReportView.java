    private DefaultComboBoxModel comboBoxModel = null;

    public void comboBoxItemsFromDatabase() {
        comboBoxModel = new DefaultComboBoxModel();
        List<ProductCategory> list = ProductCategoryService.getCatList();
        for (ProductCategory pc : list) {

            comboBoxModel.addElement(pc.getId() + " " + pc.getName());
        }
        cmbCategory.setModel(comboBoxModel);
    }

    public void displayPurchaseByCategoryIntoTable() {
        DefaultTableModel model = (DefaultTableModel) tblPurchaseByCategory.getModel();
        model.setRowCount(0);
        Object[] row = new Object[7];
        String selectCat = cmbCategory.getItemAt(cmbCategory.getSelectedIndex());
        String[] splited = selectCat.split("\\s+");
        List<Purchase> list = ReportService.getProductListBycategory(Integer.parseInt(splited[0]));
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getProductName();
            row[1] = list.get(i).getProductCode();
            row[2] = list.get(i).getQty();
            row[3] = list.get(i).getUnitprice();
            row[4] = list.get(i).getTotalPrice();
            row[5] = list.get(i).getPurchasedate();
            row[6] = list.get(i).getProductCategory().getName();

            model.addRow(row);
        }

    }

    public void displayPurchaseByDateRangeIntoTable() {
        DefaultTableModel model = (DefaultTableModel) tblPurchaseByDateRange.getModel();
        model.setRowCount(0);
        Object[] row = new Object[7];
        try {
            Date sdate = new SimpleDateFormat("yyyy-MM-dd").parse(txtStartDate.getText().trim());
            Date edate = new SimpleDateFormat("yyyy-MM-dd").parse(txtEndDate.getText().trim());
            List<Purchase> list = ReportService.getProductListByDateRange(sdate, edate);
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getProductName();
                row[1] = list.get(i).getProductCode();
                row[2] = list.get(i).getQty();
                row[3] = list.get(i).getUnitprice();
                row[4] = list.get(i).getTotalPrice();
                row[5] = list.get(i).getPurchasedate();
                row[6] = list.get(i).getProductCategory().getName();

                model.addRow(row);
            }

        } catch (Exception e) {
        }

    }

