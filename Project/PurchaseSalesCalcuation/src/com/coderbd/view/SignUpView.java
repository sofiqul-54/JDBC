    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        // TODO add your handling code here:
        String ut = "";
        if (rAdmin.isSelected() == true) {
            ut = rAdmin.getText().trim();
        } else {
            ut = rSalesman.getText().trim();
        }
        User user = new User(txtusername.getText().trim(), txtPass.getText().trim(), ut, txtFirstName.getText().trim(), txtLastName.getText().trim(), txtEmail.getText().trim(), txtMobile.getText().trim(), new Date(), true);
        UserService.insert(user);
        clearForm();
        lblMsg.setText("User Registration Succes");
        displayUserListIntoTable();
    }//GEN-LAST:event_btnSignUpActionPerformed

