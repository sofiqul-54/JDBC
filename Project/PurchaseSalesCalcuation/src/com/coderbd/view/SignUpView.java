    public void displayUserListIntoTable() {
        List<User> list = UserService.getUserList();
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        model.setRowCount(0);
        Object[] row = new Object[8];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getUsername();
            row[1] = list.get(i).getPassword();
            row[2] = list.get(i).getUserType();
            row[3] = list.get(i).getFirstName() + " " + list.get(i).getLastName();
            row[4] = list.get(i).getEmail();
            row[5] = list.get(i).getMobile();
            row[6] = list.get(i).getRegiDate();
            row[7] = list.get(i).isStatus();
            model.addRow(row);
        }
    }

